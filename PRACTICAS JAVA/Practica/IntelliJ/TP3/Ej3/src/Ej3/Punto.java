package Ej3;

public class Punto {
    private float valorX, valorY;


    public Punto(float X, float Y){
        setValorX(X);
        setValorY(Y);
    }


    public float getValorX() {
        return valorX;
    }

    public void setValorX(float valorX) {
        this.valorX = valorX;
    }

    public float getValorY() {
        return valorY;
    }

    public void setValorY(float valorY) {
        this.valorY = valorY;
    }
}

